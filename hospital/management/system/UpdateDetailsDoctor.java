    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String doctorid=jTextField1.getText();
        try {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("Select * from doctor where did='"+doctorid+"' ");
            if (rs.next()) {
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
                jTextField6.setText(rs.getString(6));
                jTextField7.setText(rs.getString(7));
                jTextField8.setText(rs.getString(8));
                jTextField9.setText(rs.getString(9));
                jPasswordField1.setText(rs.getString(10));
                jTextField11.setText(rs.getString(11));
                jTextField12.setText(rs.getString(12));
                jFormattedTextField1.setValue(rs.getString(13));
                jFormattedTextField2.setValue(rs.getString(14));
                
                
                
            }
            else
                JOptionPane.showMessageDialog(null, "Doctor id does not exist  ");
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String did=jTextField1.getText();
        String name=jTextField2.getText();
        String email=jTextField3.getText();
        String phoneno=jTextField4.getText();
        String gender=jTextField5.getText();
        String address=jTextField6.getText();
        String marital=jTextField7.getText();
        String dob=jTextField8.getText();
        String qualification=jTextField9.getText();
        String password=jPasswordField1.getText();
        String jdate=jTextField11.getText();
        String scharge=jTextField12.getText();
        String starttime=jFormattedTextField1.getText();
        String endtime=jFormattedTextField2.getText();
     
        try {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            st.executeUpdate("update doctor set name='"+name+"',email='"+email+"',phoneno='"+phoneno+"',gender='"+gender+"',address='"+address+"',marital='"+marital+"',dob='"+dob+"',qualification='"+qualification+"',password='"+password+"',jdate='"+jdate+"',scharge='"+scharge+"',starttimevalue='"+starttime+"',endtimevalue='"+endtime+"'where did='"+did+"'");
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new UpdateDetailsDoctor().setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
        
        
        
        
    }//GEN-LAST:event_jButton3ActionPerformed


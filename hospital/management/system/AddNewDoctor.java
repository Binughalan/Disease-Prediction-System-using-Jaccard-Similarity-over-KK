    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String did = labelId.getText();
        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String phoneno = jTextField3.getText();
        String gender = (String) jComboBox1.getSelectedItem();
        String address = jTextField4.getText();
        String marital = (String) jComboBox2.getSelectedItem();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-mm-yyyy");
        String dob = dateFormat.format(jDateChooser2.getDate());
        String qualification = jTextField5.getText();
        String password=jPasswordField1.getText();
        String jdate = dateFormat.format(jDateChooser1.getDate());
        String scharge = jTextField6.getText();
        String starttime=jFormattedTextField1.getText();
        String endtime=jFormattedTextField2.getText();
        
    
        try {
             Time starttimevalue = Time.valueOf(starttime + ":00");
             Time endtimevalue = Time.valueOf(endtime + ":00");
            
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into doctor values('" + did + "','" + name + "','" + email + "','" + phoneno + "','" + gender + "','" + address + "','" + marital + "','" + dob + "','" + qualification + "','" + password + "','" + jdate + "','" + scharge + "','" + starttimevalue + "','" + endtimevalue + "')");
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new AddNewDoctor().setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String pid=jTextField1.getText();
        try {
            ResultSet rs=Select.getData("Select*from inpatient where pid='"+pid+"'and checkout is NULL ");
            if (rs.next()) {
                jTextField1.setEditable(true);
                id=rs.getInt(1);
                jTextField2.setText(rs.getString("name"));
                jTextField5.setText(rs.getString("phoneno"));
                jTextField6.setText(rs.getString("charge"));
                jTextField8.setText(rs.getString("scharge"));
                
                
                java.util.Date out_date = jDateChooser1.getDate();
                Long l1 = out_date.getTime();
                java.sql.Date ch_date = new java.sql.Date(l1);
                
                java.util.Date uissue_date = jDateChooser1.getDate();
                Long l2 = uissue_date.getTime();
               java.sql.Date c_date = new java.sql.Date(l2);
               
               long difference=ch_date.getTime()-uissue_date.getTime();
               int day=(int) (difference/(1000*60*60*24));
               if(day==0)
                   day=1;
               jTextField7.setText(String.valueOf(day));
               float charge=Float.parseFloat(jTextField6.getText());
               float scharge= Float.parseFloat(jTextField8.getText());
               
               jTextField9.setText(String.valueOf((day*charge)+scharge));
               type=rs.getString("type");
      
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Patient id doesn't exist");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


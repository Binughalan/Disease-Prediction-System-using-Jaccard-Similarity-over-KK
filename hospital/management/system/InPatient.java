    public void roomDetails() {
        jComboBox4.removeAllItems();
        jTextField6.setText("");
        type = (String) jComboBox3.getSelectedItem();
        try {
            ResultSet rs = Select.getData("Select * from room where type='" + type + "'");
            while (rs.next()) {
                jComboBox4.addItem(rs.getString(1));

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    public void validateFields() {

        String name = jTextField1.getText();
        String age = jTextField2.getText();
        String phoneno = jTextField3.getText();
        String address = jTextField4.getText();

        if (!name.equals("") && !age.equals("") && phoneno.matches(mobileNumberPattern) && phoneno.length() == 10 && !address.equals("")) {
            btnSave.setEnabled(true);
        } else {
            btnSave.setEnabled(true);
        }

    }


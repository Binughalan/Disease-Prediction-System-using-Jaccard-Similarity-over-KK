    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String pid = jLabel3.getText();
        String name = jTextField1.getText();
        String gender = (String) jComboBox1.getSelectedItem();
        String age = jTextField2.getText();
        String phoneno = jTextField3.getText();
        String address = jTextField4.getText();
        String blood = (String) jComboBox2.getSelectedItem();
//        Date date = jDateChooser1.getDate();
        java.util.Date uissue_date = jDateChooser1.getDate();
        Long l1 = uissue_date.getTime();
        java.sql.Date c_date = new java.sql.Date(l1);
        String type = (String) jComboBox3.getSelectedItem();
        String roomno = (String) jComboBox4.getSelectedItem();
        String charge = jTextField6.getText();
        String time = jTextField7.getText();
        String doctor = jTextField8.getText();
        String scharge = jTextField9.getText();

        try {

            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into inpatient(pid,name,gender,age,phoneno,address,blood,date,type,roomno,charge,time,doctor,scharge) values('" + pid + "','" + name + "','" + gender + "','" + age + "','" + phoneno + "','" + address + "','" + blood + "','" + c_date + "','" + type + "','" + roomno + "','" + charge + "','" + time + "','" + doctor + "','" + scharge + "')");
            
            JOptionPane.showMessageDialog(null, "Successfully Updated");
            setVisible(false);
            new InPatient().setVisible(true);

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

